// AND
console.log(true && true);
console.log(true && false);
console.log(false && false);
console.log();

// OR
console.log(true || true);
console.log(true || false);
console.log(false || false);
console.log();

// NOT
console.log(!true);
console.log(!false);
console.log("===");

// >, <, >=, <=, ==, !=, ===, !==
console.log(2 > 1);
console.log(3 < 1);
console.log(3 >= 3);
console.log(1.0 <= 0.9999);
console.log("3" == 3); // true => 타입은 신경 쓰지 않음
console.log("3" === 3); // false =>타입도 신경 씀
console.log(3 != "3"); // false
console.log(3 !== "3"); // true
